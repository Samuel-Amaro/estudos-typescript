/**
 * * AS PRIMITIVAS: STRING, NUMBER AND BOOLEAN
 * 
 *  JavaScript tem três primitivos muito comumente usados : string, number e boolean. Cada um tem um tipo correspondente no TypeScript. Como você pode esperar, esses são os mesmos nomes que você veria se usasse o typeof operador JavaScript em um valor desses tipos:
 * 
    * string representa valores de string como "Hello, world" 
    * 
    * number é para números como 42. JavaScript não tem um valor de tempo de execução especial para números inteiros, então não há equivalente a int ou float- tudo é simplesmente number
    * 
    * boolean é para os dois valores true e false
    * 
 * Os nomes de tipo String, Number e Boolean(começando com letras maiúsculas) são legais, mas referem-se a alguns tipos integrados especiais que raramente aparecerão em seu código. Sempre use string, number ou boolean para tipos. 
*/