/**
 * * CRIANDO TIPOS A PARTIR DE TIPOS
 * 
 *  O sistema de tipos do TypeScript é muito poderoso porque permite expressar tipos em termos de outros tipos.
 * 
 * A forma mais simples dessa ideia são os genéricos; na verdade, temos uma grande variedade de operadores de tipo disponíveis para uso. Também é possível expressar tipos em termos de valores que já temos.
 * 
 * Ao combinar vários tipos de operadores, podemos expressar operações e valores complexos de maneira sucinta e sustentável. Nesta seção, abordaremos maneiras de expressar um novo tipo em termos de um tipo ou valor existente.
 * 
    *  Genéricos(Generics) - Tipos que recebem parâmetros
    * 
    * Keyof Type Operator - Usando o keyof operador para criar novos tipos
    * 
    * Typeof Type Operator - Usando o typeof operador para criar novos tipos
    * 
    * Tipos de acesso indexados(Indexed Access Types) - usando Type['a'] a sintaxe para acessar um subconjunto de um tipo
    * 
    * Tipos condicionais(Conditional Types ) - Tipos que agem como instruções if no sistema de tipos
    * 
    * Tipos mapeados(Mapped Types) - Criando tipos mapeando cada propriedade em um tipo existente
    * 
    * Tipos literais de modelo(Template Literal Types) - tipos mapeados que alteram propriedades por meio de strings literais de modelo
*/